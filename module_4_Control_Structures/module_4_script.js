
// Conditional Statements
let score = 85;
if (score > 90) {
    console.log('Excellent');
} else if (score > 75) {
    console.log('Very Good');
} else {
    console.log('Good');
}

// Looping Structures
console.log('For Loop:');
for (let i = 0; i < 5; i++) {
    console.log(i);
}

console.log('While Loop:');
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}

console.log('Do-While Loop:');
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);
